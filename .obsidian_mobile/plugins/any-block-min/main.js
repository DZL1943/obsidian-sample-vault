function listdata2dirdata(list) {
  const is_have_vbar = [];
  const newlist = [];
  for (let i = 0; i < list.length; i++) {
    const item = list[i];
    let type;
    if (item.content.endsWith("/")) {
      type = "folder";
    } else {
      const parts = item.content.split(".");
      if (parts.length === 0 || parts[parts.length - 1] === "")
        type = "";
      else
        type = parts[parts.length - 1];
    }
    let is_last = true;
    for (let j = i + 1; j < list.length; j++) {
      if (list[j].level < item.level) {
        is_last = true;
        break;
      } else if (list[j].level == item.level) {
        is_last = false;
        break;
      } else {
        continue;
      }
    }
    is_have_vbar[item.level] = !is_last;
    let pre_as_text = "";
    if (item.level > 1) {
      for (let i2 = 1; i2 < item.level; i2++) {
        if (!is_have_vbar.hasOwnProperty(i2))
          pre_as_text += "[e]";
        else if (is_have_vbar[i2])
          pre_as_text += "|  ";
        else
          pre_as_text += "   ";
      }
    }
    newlist.push({
      content: item.content,
      level: item.level,
      type,
      is_last,
      pre_as_text
    });
  }
  return newlist;
}
